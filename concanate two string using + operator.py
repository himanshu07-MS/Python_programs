# concanate two string using + operator.

university = input('enter university name:')
print('university name is:',university)

address = input('enter address name:')
print('university address name is:',address)

string_concanate = university + address               # concanate of two strings

print('string concanate:',string_concanate)            # refers the values of string_concanate

print('class type is :', type(string_concanate))       # refers data types of string_concanate

print('id is :',id(string_concanate))                          # refers id of string_concanate
